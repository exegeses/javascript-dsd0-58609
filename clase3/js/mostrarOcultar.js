//ubicar elemento dentro del DOM
let caja = document.getElementById('caja');

//declaración de función/es de control
function ocultar()
{
    //display:none;
    //visibility:hidden
    caja.style.display = 'none';
}
function mostrar()
{
    //display:block;
    //visibility:visible
    caja.style.display = 'block';    
}